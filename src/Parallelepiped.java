public class Parallelepiped {
    public static void main(String[] args) {
        int a = 10;
        int b = 15;
        int c = 20;
        int volume = a * b * c;
        int length = a + b + c;

        System.out.println("Volume of the parallelepiped = " + volume);
        System.out.println("The sum of the lengths of all sides = " + length);
    }
}
